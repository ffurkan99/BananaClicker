class FloatingEffect {
  final int id;
  final String text;
  final double xOffset;
  final double yOffset;
  final bool isCritical;
  final DateTime createdAt;

  FloatingEffect({
    required this.id,
    required this.text,
    required this.xOffset,
    required this.yOffset,
    required this.isCritical,
    required this.createdAt,
  });
}
